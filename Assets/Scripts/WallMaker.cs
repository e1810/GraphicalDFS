using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class WallMaker : MonoBehaviour
{
    bool[,] wall = new bool[30, 30];
    bool[,] walled = new bool[30, 30];
    string[] s = {"....#####.....######..........",
"##....#######..#####.####.####",
"##.##...........####.####.....",
"##..#####.##########.#########",
"###..####.########...#####....",
"####.####.......##.####....###",
"........#######....####.######",
".######.######..#######..#####",
".######.....##.#########.#####",
".######.###.########.....#####",
"..#####..##....#####.#########",
"#..#####.#####.......#########",
"##.....#############.....#####",
"######.......##########.######",
"######.#####.....######..#####",
"######.#########...#####...###",
"####......########.#######.###",
"####.####.####.....#####......",
".....##.....#########....####.",
".######.###.##....######.####.",
"......#####....########...####",
".#########..####......########",
"....######.##########..#####..",
"###...####.......#####.######.",
"####....#..#####..#....#####..",
"###..#####.....####.######...#",
"####.....#####......####...###",
"########.....####.####...#####",
"############..###......#######",
"#####################........."
};
    public GameObject newWall;

    struct Point
    {
        public int x, y;
    };
    Stack<Point> stk = new Stack<Point>();

    // Start is called before the first frame update
    void Start()
    {
        Debug.Log(wall);
        for(int i=0; i<30; i++)
        {
            for(int j=0; j<30; j++)
            {
                wall[i,j] = (s[i][j] == '#');

            }
        }
        Point o;
        o.x = o.y = 0;
        stk.Push(o);
    }

    // Update is called once per frame
    void Update()
    {
        for(int i=0; i<30; i++)
        {
            for(int j=0; j<30; j++)
            {
                if (wall[i,j] && (!walled[i,j]))
                {
                    Instantiate(newWall, new Vector3(15f-j, 1f, i), Quaternion.identity);
                    walled[i, j] = true;
                }
            }
        }
    }
}
